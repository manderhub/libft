#include "libft.h"

/**
 * Counts the number of nodes in a list.
 * 
 * lst:	beginning of the list
*/
int	ft_lstsize(t_list *lst)
{
	int	size;

	size = 0;
	while (lst)
	{
		lst = lst->next;
		size++;
	}
	return (size);
}
